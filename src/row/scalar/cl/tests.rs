//! Reference cross-checks for the BT.2020 constant-luminance (`YcCbcCrc`)
//! non-affine decode.
//!
//! The decode is pinned against `colour-science` 0.4.7 — the authoritative
//! BT.2020 CL implementation (`colour.models.rgb.ycbcr.YcCbcCrc_to_RGB` /
//! `oetf_BT2020`) — plus ITU-R BT.2020-2 structural anchors (gray-axis
//! neutrality, OETF round-trip, weight-derivation consistency) that hold
//! independently of any library.
//!
//! Domain convention: `colour-science`'s `YcCbcCrc_to_RGB` returns **linear**
//! RGB (the stage before colconv's transfer-preserving OETF re-encode), so the
//! reference vectors below pin [`cl_norm_to_rgb_linear`] (the linear stage);
//! the re-encode to `R'G'B'` and the integer narrow are pinned separately.
//! Studio (`legal`) range corresponds to `full_range = false`; full range to
//! `full_range = true` — the same correspondence
//! [`crate::row::scalar::ictcp`] uses. All reference values were generated by
//! `colour.models.rgb.ycbcr.YcCbcCrc_to_RGB(..., in_int=True)` with the
//! matching `in_bits` / `in_legal` / `is_12_bits_system`.

use super::*;

use crate::resample::bt2020_oetf;

/// f32 tolerance for the linear-RGB reference cross-check: the decode is f32
/// throughout and `colour-science` is f64, so the gap is a few ULP after the
/// transcendental inverse-OETF + the green solve.
const LIN_TOL: f32 = 5e-6;

/// Helper: full integer-input → linear-RGB decode at 12-bit, mirroring the
/// kernel's `dequant_cl` + `cl_norm_to_rgb_linear` (the `colour-science`
/// comparison stage).
fn decode_linear<const BITS: u32>(
  yc: u16,
  cbc: u16,
  crc: u16,
  full_range: bool,
  system: ClSystem,
) -> [f32; 3] {
  cl_norm_to_rgb_linear(dequant_cl::<BITS>(yc, cbc, crc, full_range), system)
}

fn assert_close(got: [f32; 3], want: [f32; 3], tol: f32, what: &str) {
  for i in 0..3 {
    assert!(
      (got[i] - want[i]).abs() <= tol,
      "{what}: channel {i} = {} (want {}, |Δ| = {})",
      got[i],
      want[i],
      (got[i] - want[i]).abs()
    );
  }
}

// ---- colour-science `YcCbcCrc_to_RGB` linear-RGB reference vectors -------

/// 12-bit studio-range mid-code: `colour.YcCbcCrc_to_RGB([2048, 2148, 1948],
/// in_bits=12, in_legal=True, in_int=True, is_12_bits_system=True)`.
#[test]
fn decode_matches_colour_science_12bit_studio_midcode() {
  let system = ClSystem::resolve(Primaries::Bt2020, Transfer::Bt2020_12Bit).unwrap();
  let got = decode_linear::<12>(2048, 2148, 1948, false, system);
  assert_close(
    got,
    [0.225_844, 0.284_305, 0.316_264],
    LIN_TOL,
    "12-bit studio mid-code",
  );
}

